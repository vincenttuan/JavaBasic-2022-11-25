package day1;

public class Hello6 {

	public static void main(String[] args) {
		// int 整數宣告
		int r = 15; // 半徑
		// double 浮點數(小數)宣告
		// final 表示不可改變
		final double pi = 3.1415926; // 圓周率
		// 計算圓面積
		double area = r * r * pi;
		// 印出圓面積
		System.out.println("圓面積: " + area);
		
	}

}